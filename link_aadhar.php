            <h1>Link Aadhaar User Manual</h1>
            
            <h2>1. Overview</h2>
            <p>
                For new applicants of PAN card, the Aadhaar PAN linking is done automatically during the application stage. 
                For existing PAN holders who were allotted PAN on or before 01-07-2017, it is mandatory to link PAN with Aadhaar. 
                The Link Aadhaar service is available to individual taxpayers (both registered and unregistered on the e-Filing Portal). 
                If you do not link your PAN with Aadhaar by 30<sup>th</sup> June 2023, your PAN will become inoperative. 
                However, people who fall under the exempted category will not be subject to the effects of PAN becoming inoperative.
            </p>
            
            <h2>2. Prerequisites for availing this service:</h2>
            <ol>
                <li>Valid PAN</li>
                <li>Aadhaar number</li>
                <li>Valid mobile number</li>
            </ol>
            
            <h2>3. How to make payment of Aadhaar PAN link fee on e-Filing Portal</h2>
            <p class="steps">
                Step 1: Visit the e-Filing Portal Home page and click on 
                <a href="https://www.incometax.gov.in/iec/foportal/" target="_blank" class="link">Link Aadhaar</a> 
                in the Quick Links section. Alternatively, login to the e-filing portal and click on 
                <a href="https://www.incometax.gov.in/iec/foportal/" target="_blank" class="link">Link Aadhaar</a> 
                in the Profile section.
            </p>
